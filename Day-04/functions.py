# Addition
def addition(a, b):
    add = a + b
    print("Addition: ", add)
    
# Substraction
def subtraction(a, b):
    sub = a - b
    print("Subtraction: ", sub)

# Multiplication
def muliplication(a, b):
    mul = a * b
    print("Multiplication: ", mul)

# Division
def division(a, b):
    div = a / b
    print("Division:", div)

addition(10, 2)
subtraction(30, 20)
muliplication(10, 10)
division(20, 10)