# Integer values
num1 = 20
num2 = 10

# Addition
result1 = num1 + num2
print("Addition: ", result1)

# Substraction
result2 = num1 - num2
print("Substraction", result2)

# Multiplication
result7 = num1 * num2
print("Multiplication: ", result7)

# Division
result3 = num1 / num2
print("Division: ", result3)

# Integer division
result4 = num1 // num2
print("Interger division: ", result4)

# Modulus(reminder)
result5 = num1 % num2
print("Modulus: ", result5)

# Absolute value
result6 = abs(-7)
print("Absolute value: ", result6)